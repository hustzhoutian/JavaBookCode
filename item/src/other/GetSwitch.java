package other;


public class GetSwitch {
	public static void main(String[] args) {
		int week = 2;
		switch(week){
		case 1:
			System.out.println("M");
			break;
		case 2:
			System.out.println("T");
			break;
		case 3:
			System.out.println("W");
			break;
		default:
			System.out.println("Do not know");
			break;
		}
	}

}
